def get_country_name(country_code):
    return country_names.get(country_code.upper(), country_code.upper())

def parse_provider_from_tag(tag):
    """Extract provider name from tag, e.g. '🇯🇵 Oracle Corp 2' → 'Oracle Corp'."""
    m = re.match(r"([\U0001F1E6-\U0001F1FF]{2})\s*([^\d]+?)(?:\s+\d+)?$", tag)
    if m:
        return m.group(2).strip()
    return "-"

def clean_tag(tag):
    tag = re.sub(r'\s*\d+$', '', tag)
    tag = tag.replace('"', '')
    tag = re.sub(r'\s+', ' ', tag)
    tag = tag.strip()
    return tag

def flag_emoji_to_country_code(flag):
    # "🇸🇬" -> "SG"
    if len(flag) != 2:
        return ""
    return ''.join([chr(ord(c) - 0x1F1E6 + ord('A')) for c in flag])

def get_country_code_from_tag(tag):
    m = re.match(r"([\U0001F1E6-\U0001F1FF]{2})", tag)
    if m:
        return flag_emoji_to_country_code(m.group(1))
    return ""

def format_and_clean_nodes(live_nodes_sorted):
    """
    Format tag and clean nodes:
    - tag: '[flag] [provider] [urut]'
    - remove 'provider' and 'country' fields
    """
    final_nodes = []
    for idx, node in enumerate(live_nodes_sorted, 1):
        provider = node.get("provider", "-")
        country = node.get("country", "-")
        # fallback provider/country dari tag jika kosong
        if (not provider or provider == "-") and node.get("tag"):
            provider = parse_provider_from_tag(node.get("tag", ""))
        if (not country or country == "-") and node.get("tag"):
            country = get_country_code_from_tag(node.get("tag", ""))
        flag = country_to_flag(country)
        tag_bersih = clean_tag(f"{flag} {provider}")
        node["tag"] = f"{tag_bersih} {idx}"
        node.pop("provider", None)
        node.pop("country", None)
        final_nodes.append(node)
    return final_nodes